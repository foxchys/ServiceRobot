/*************************595发送数据进程(红）***************************************/
void send595r(uchar dat1,uchar dat2)       
{
 int i;
 char temp_dat;
 temp_dat=dat1;	
 oe=0;		   				//要发送的数据1 
 for(i=0;i<8;i++)
  {						   	//开始发送数据
    SH_CP=0;
    if(temp_dat&0x80)
      { DSR0=1; } 
    else
      { DSR0=0; } 	   		//发送下一个数据
    SH_CP=1;
    temp_dat=temp_dat<<1;  	//数据左移
  }
 temp_dat=dat2;			   	//要发送的数据2
 for(i=0;i<8;i++)
  {						   	//开始发送数据
    SH_CP=0;
    if(temp_dat&0x80)
      { DSR0=1;  } 
    else
	  { DSR0=0; } 	   		//发送下一个数据
    SH_CP=1;
    temp_dat=temp_dat<<1;  	//数据左移
  }	
  oe=1;
}	
